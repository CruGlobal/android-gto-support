package org.ccci.gto.android.common;

public final class TimeConstants {
    public static final long MIN_IN_MS = 60 * 1000;
    public static final long HOUR_IN_MS = 60 * MIN_IN_MS;
    public static final long DAY_IN_MS = 24 * HOUR_IN_MS;
    public static final long WEEK_IN_MS = 7 * DAY_IN_MS;
}
